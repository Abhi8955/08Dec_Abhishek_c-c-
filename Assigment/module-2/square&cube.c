//wap square & cube
#include<stdio.h>
int main()
{
	int no,s,c;
	printf("enter the no:");
	scanf("%d",&no);
    
	printf("----------square----------");
	s=no*no;
	printf("\n square:%d",s);
	
	c=no*no*no;
	printf("\n----------cube----------");
    printf("\n cube:%d",c);
	
	return 0;
	
	
	
}
